// Contar de 0 a 10
let inicioDaContagem = 4
let contador = inicioDaContagem

// While = enquanto
// O computador repetira o codigo dentro de {} enquanto a condição for verdadeira. 
// No nosso, o contador vai ate o limite 100, se passar disso ele para, porque 101 e maior que 100, portanto contador <= 100 e falso.
while (contador <= 10) {
    console.log(contador);
    contador = contador + 2
}
